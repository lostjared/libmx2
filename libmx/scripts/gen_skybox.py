def normalize(v):
    x, y, z = v
    length = math.sqrt(x*x + y*y + z*z)
    if length == 0.0:
        return (0.0, 0.0, 0.0)
    return (x / length, y / length, z / length)

verts = []
norms = []

for v in base_verts:
    nx, ny, nz = normalize(v)
    vx = nx * size
    vy = ny * size
    vz = nz * size
    verts.append((vx, vy, vz))
    norms.append((nx, ny, nz))

